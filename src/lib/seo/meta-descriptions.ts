// Function to get meta description by currency pair and locale
export function getMetaDescription(
  fromCurrency: string,
  toCurrency: string,
  amount?: number,
  locale: string = 'en'
): { title: string; description: string; keywords: string[] } {
  const key = amount
    ? `${amount}-${fromCurrency.toLowerCase()}-${toCurrency.toLowerCase()}${locale === 'de' ? '-de' : ''}`
    : `${fromCurrency.toLowerCase()}-${toCurrency.toLowerCase()}${locale === 'de' ? '-de' : ''}`;

  const meta = currencyMetaDescriptions[key as keyof typeof currencyMetaDescriptions];

  if (meta) {
    return meta;
  }

  // Fallback for unsupported pairs
  const currencies = {
    'eur': locale === 'de' ? 'Euro' : 'Euro',
    'usd': locale === 'de' ? 'US Dollar' : 'US Dollar',
    'gbp': locale === 'de' ? 'Britisches Pfund' : 'British Pound',
    'jpy': locale === 'de' ? 'Japanischer Yen' : 'Japanese Yen',
    'chf': locale === 'de' ? 'Schweizer Franken' : 'Swiss Franc',
    'cad': locale === 'de' ? 'Kanadischer Dollar' : 'Canadian Dollar'
  };

  const fromName = currencies[fromCurrency.toLowerCase() as keyof typeof currencies] || fromCurrency.toUpperCase();
  const toName = currencies[toCurrency.toLowerCase() as keyof typeof currencies] || toCurrency.toUpperCase();

  if (locale === 'de') {
    return {
      title: `${fromName} ${toName} Rechner - Aktueller ${fromCurrency.toUpperCase()}/${toCurrency.toUpperCase()} Wechselkurs`,
      description: `${fromName} in ${toName} umrechnen mit aktuellem Wechselkurs. Kostenloser Währungsrechner mit Live-Kursen alle 15 Minuten aktualisiert.`,
      keywords: [`${fromCurrency.toLowerCase()} ${toCurrency.toLowerCase()}`, 'währungsrechner', 'wechselkurs', 'umrechnen']
    };
  }

  return {
    title: `${fromName} to ${toName} Converter - Live ${fromCurrency.toUpperCase()}/${toCurrency.toUpperCase()} Exchange Rate`,
    description: `Convert ${fromCurrency.toUpperCase()} to ${toCurrency.toUpperCase()} with today's live exchange rate. Free ${fromName} to ${toName} calculator with real-time rates updated every 15 minutes.`,
    keywords: [`${fromCurrency.toLowerCase()} to ${toCurrency.toLowerCase()}`, 'currency converter', 'exchange rate', 'calculator']
  };
}

