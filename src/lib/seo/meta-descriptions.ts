// Generate structured data for specific conversion pages
export function generateConversionStructuredData(
  fromCurrency: string,
  toCurrency: string,
  rate: number,
  amount: number = 1,
  locale: string = 'en'
) {
  const currencies = {
    'eur': locale === 'de' ? 'Euro' : 'Euro',
    'usd': locale === 'de' ? 'US-Dollar' : 'US Dollar',
    'gbp': locale === 'de' ? 'Britisches Pfund' : 'British Pound',
    'jpy': locale === 'de' ? 'Japanischer Yen' : 'Japanese Yen',
    'chf': locale === 'de' ? 'Schweizer Franken' : 'Swiss Franc'
  };

  const fromName = currencies[fromCurrency.toLowerCase() as keyof typeof currencies] || fromCurrency;
  const toName = currencies[toCurrency.toLowerCase() as keyof typeof currencies] || toCurrency;

  return {
    "@context": "https://schema.org",
    "@type": ["WebApplication", "FinancialService"],
    "name": locale === 'de'
      ? `${fromName} ${toName} Rechner`
      : `${fromName} to ${toName} Converter`,
    "description": locale === 'de'
      ? `${amount} ${fromName} in ${toName} umrechnen mit aktuellem Wechselkurs`
      : `Convert ${amount} ${fromName} to ${toName} with current exchange rate`,
    "applicationCategory": "FinanceApplication",
    "operatingSystem": "Web Browser",
    "offers": {
      "@type": "Offer",
      "price": "0",
      "priceCurrency": "USD"
    },
    "featureList": [
      locale === 'de' ? "Live-Wechselkurse" : "Live exchange rates",
      locale === 'de' ? "Offline-Funktionalität" : "Offline functionality",
      locale === 'de' ? "Kostenlose Nutzung" : "Free to use",
      locale === 'de' ? "Mobile optimiert" : "Mobile optimized"
    ],
    "mainEntity": {
      "@type": "ExchangeRateSpecification",
      "currency": fromCurrency.toUpperCase(),
      "currentExchangeRate": {
        "@type": "UnitPriceSpecification",
        "price": rate.toString(),
        "priceCurrency": toCurrency.toUpperCase()
      },
      "validFrom": new Date().toISOString(),
      "validThrough": new Date(Date.now() + 15 * 60 * 1000).toISOString()
    }
  };
}
